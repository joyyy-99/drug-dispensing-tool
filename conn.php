/* Element selector: Targets all <p> elements */
p {
  color: blue;
}

/* Class selector: Targets elements with the class "highlight" */
.highlight {
  background-color: yellow;
}

/* ID selector: Targets the element with the ID "header" */
#header {
  font-size: 24px;
}

/* Attribute selector: Targets elements with the "type" attribute set to "submit" */
input[type="submit"] {
  background-color: green;
}

/* Pseudo-class selector: Targets the first child of an element */
ul li:first-child {
  font-weight: bold;
}

/* Pseudo-element selector: Targets the first line of a paragraph */
p::first-line {
  text-transform: uppercase;
}

